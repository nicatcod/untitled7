package MethodDersi;
public class Methods  {

    public int topla(int a, int b) {
        return a + b;
    }

    public int cix(int a, int b) {
        return a - b;
    }

    public long vurma(long a, long b) {
        return a * b;
    }

    public double bolme(int a, int b) {
        return a / b;
    }
}